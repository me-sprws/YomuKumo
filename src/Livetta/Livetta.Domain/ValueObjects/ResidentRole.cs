namespace Livetta.Domain.ValueObjects;

/// <summary>
/// Роль резидента в апартаменте.
/// Один и тот же человек может быть связан с разными апартаментами в разных ролях.
/// </summary>
public enum ResidentRole
{
    /// <summary>
    /// Жилец. Проживает в апартаменте, но не является собственником или арендатором.
    /// </summary>
    Occupant = 0,

    /// <summary>
    /// Арендатор. Использует апартамент на основании договора аренды.
    /// </summary>
    Tenant = 1,

    /// <summary>
    /// Владелец недвижимости. Имеет полные права распоряжаться апартаментом.
    /// </summary>
    Owner = 2,
}